#include "Memory.h"

Memory::Memory()
{
    _data.fill(0);
}

const uint8_t Memory::operator[](uint16_t address) const
{
    return _data.at(address);
}

uint8_t& Memory::operator[](uint16_t address)
{
    return _data.at(address);
}
